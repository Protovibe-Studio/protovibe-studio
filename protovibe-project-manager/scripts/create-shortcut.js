#!/usr/bin/env node
// Creates a desktop launcher for Protovibe.
//
// Path-independence: the project root is written to ~/.protovibe/project-path
// during install. The launcher reads that file at runtime, so re-running the
// installer after moving the repo is enough to fix the shortcut.
//
// macOS  → .mac-installer/Protovibe.app (drag to /Applications)
// Win    → %USERPROFILE%\.protovibe\Protovibe.bat + Desktop\Protovibe.lnk
// Linux  → ~/Desktop/Protovibe.desktop
import os from 'node:os';
import path from 'node:path';
import fs from 'node:fs';
import { fileURLToPath } from 'node:url';
import { execSync, spawnSync } from 'node:child_process';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

// Monorepo root (one above protovibe-project-manager/)
const PROJECT_ROOT = path.resolve(__dirname, '..', '..');
const ASSETS_DIR = path.join(__dirname, 'assets');
const APP_NAME = 'Protovibe';

const HOME = os.homedir();
const PROTOVIBE_CONFIG_DIR = path.join(HOME, '.protovibe');
const PROJECT_PATH_FILE = path.join(PROTOVIBE_CONFIG_DIR, 'project-path');

const c = {
  reset: '\x1b[0m', bold: '\x1b[1m',
  green: '\x1b[32m', yellow: '\x1b[33m', red: '\x1b[31m', cyan: '\x1b[36m',
};
const log = {
  info: (m) => console.log(`${c.cyan}${m}${c.reset}`),
  warn: (m) => console.log(`${c.yellow}⚠  ${m}${c.reset}`),
  ok:   (m) => console.log(`${c.green}${c.bold}✔ ${m}${c.reset}`),
  err:  (m) => console.error(`${c.red}✖ ${m}${c.reset}`),
};

function writeProjectPathConfig() {
  fs.mkdirSync(PROTOVIBE_CONFIG_DIR, { recursive: true });
  fs.writeFileSync(PROJECT_PATH_FILE, PROJECT_ROOT + '\n', 'utf8');
  log.info(`Wrote project path to ${PROJECT_PATH_FILE}`);
}

function resolveDesktopDir() {
  if (os.platform() === 'win32') {
    try {
      const result = execSync(
        `powershell.exe -NoProfile -ExecutionPolicy Bypass -Command "[System.Environment]::GetFolderPath('Desktop')"`,
        { stdio: 'pipe', encoding: 'utf8' }
      ).trim();
      if (result && fs.existsSync(result)) return result;
    } catch {}
  }
  const candidates = [
    path.join(HOME, 'Desktop'),
    path.join(HOME, 'OneDrive', 'Desktop'),
    path.join(HOME, 'iCloud Drive', 'Desktop'),
    path.join(HOME, 'Library', 'Mobile Documents', 'com~apple~CloudDocs', 'Desktop'),
  ];
  for (const p of candidates) if (fs.existsSync(p)) return p;
  log.warn(`Desktop directory not found — falling back to: ${HOME}`);
  return HOME;
}

function checkIcon(filename) {
  const p = path.join(ASSETS_DIR, filename);
  if (!fs.existsSync(p)) {
    log.warn(`Icon asset missing: scripts/assets/${filename} (proceeding without custom icon)`);
    return null;
  }
  return p;
}

// ───────────────────────────── macOS ─────────────────────────────
function writeMacLauncherScript() {
  // The launcher lives under ~/.protovibe/launch.sh and is invoked by the
  // shim baked into Protovibe.app (Platypus Text Window). Keeping the real
  // logic out of the bundle means we can update it on every install without
  // re-signing the .app.
  //
  // Output goes into an NSTextView (no PTY), so colors are guarded by a
  // [ -t 1 ] check — under Platypus we get plain text, under Terminal we
  // get colors. clear/banner art still renders in both cases.
  const launcherPath = path.join(PROTOVIBE_CONFIG_DIR, 'launch.sh');
  const script = `#!/usr/bin/env bash
# Protovibe launcher — generated by create-shortcut.js. Do not edit by hand.
set -o pipefail

if [ -t 1 ]; then
  C_RESET=$'\\033[0m'; C_BOLD=$'\\033[1m'
  C_YELLOW=$'\\033[33m'; C_CYAN=$'\\033[36m'
  do_clear() { clear; }
else
  C_RESET=''; C_BOLD=''; C_YELLOW=''; C_CYAN=''
  do_clear() { :; }
fi

CFG="$HOME/.protovibe/project-path"
if [ ! -f "$CFG" ]; then
  echo "Protovibe is not configured. Run ./install.sh from the project first."
  exit 1
fi
ROOT="$(cat "$CFG")"
if [ ! -d "$ROOT" ]; then
  echo "Protovibe folder not found at: $ROOT"
  echo "If you moved it, re-run ./install.sh from the new location."
  exit 1
fi

print_banner() {
  do_clear
  printf '\\n%s' "$C_CYAN"
  cat <<'LOGO'
   ▄▄▄▄▄▄▄▄                                        ▄▄
   ███▀▀▀███                ██                 ▀▀  ██
   ███   ███ ████▄ ▄█████▄ ▀██▀▀ ▄█████▄ ██ ██ ██  ████▄ ▄█▀█▄
   ███  ▀▀▀  ██ ▀▀ ██   ██  ██   ██   ██ ██▄██ ██  ██ ██ ██▄█▀
   ███       ██    ▀█████▀  ██   ▀█████▀  ▀█▀  ██▄ ████▀ ▀█▄▄▄
LOGO
  printf '%s\\n' "$C_RESET"
  printf '%s   Keep this window open while using Protovibe in your browser.%s\\n\\n' "$C_YELLOW" "$C_RESET"
}

# Already running? The project-manager exposes /api/projects, so a 200 there
# means a Protovibe dev server is live on the default port. Just open the
# browser and exit instead of spawning a duplicate.
PROTOVIBE_URL="http://127.0.0.1:5173/"
if curl -fsS --max-time 2 "\${PROTOVIBE_URL}api/projects" >/dev/null 2>&1; then
  print_banner
  printf '   %sAlready running — opening browser…%s\\n\\n' "$C_BOLD" "$C_RESET"
  open "$PROTOVIBE_URL"
  exit 0
fi

# Make the bundled Node + pnpm visible without relying on the user's rc
# being sourced. install.sh symlinks them into ~/.local/bin.
case ":$PATH:" in
  *":$HOME/.local/bin:"*) ;;
  *) export PATH="$HOME/.local/bin:$PATH" ;;
esac

print_banner
printf '   %sOpen your browser and go to URL: %s%s\\n\\n' "$C_BOLD" "$PROTOVIBE_URL" "$C_RESET"

export NODE_NO_WARNINGS=1
cd "$ROOT"

# Apply a staged project-manager update if one is pending. The updater writes
# to protovibe-project-manager.pending/ so the running vite never sees its own
# files change. Here, before vite boots, we swap dirs and run pnpm install to
# reconcile dependencies. node_modules is preserved across the swap so install
# is a fast no-op when deps are unchanged.
PM_DIR="protovibe-project-manager"
PENDING="$PM_DIR.pending"
if [ -d "$PENDING" ]; then
  printf '   %sApplying staged project-manager update…%s\\n' "$C_CYAN" "$C_RESET"
  if [ -d "$PM_DIR/node_modules" ] && [ ! -d "$PENDING/node_modules" ]; then
    mv "$PM_DIR/node_modules" "$PENDING/node_modules"
  fi
  rm -rf "$PM_DIR.old"
  if mv "$PM_DIR" "$PM_DIR.old" && mv "$PENDING" "$PM_DIR"; then
    rm -rf "$PM_DIR.old"
    printf '   %sInstalling dependencies…%s\\n' "$C_CYAN" "$C_RESET"
    pnpm --dir "$PM_DIR" install --prefer-offline
  else
    printf '   Failed to apply staged update; reverting.\\n'
    [ -d "$PM_DIR.old" ] && [ ! -d "$PM_DIR" ] && mv "$PM_DIR.old" "$PM_DIR"
  fi
fi

exec pnpm --dir protovibe-project-manager dev 2>&1 | tee "$ROOT/dev.log"
`;
  fs.mkdirSync(PROTOVIBE_CONFIG_DIR, { recursive: true });
  fs.writeFileSync(launcherPath, script, 'utf8');
  fs.chmodSync(launcherPath, 0o755);
  return launcherPath;
}

function createMacShortcut() {
  const installerDir = path.join(PROJECT_ROOT, '.mac-installer');
  const appPath = path.join(installerDir, `${APP_NAME}.app`);
  const symlinkPath = path.join(installerDir, 'Applications');
  const templateApp = path.join(ASSETS_DIR, 'Protovibe-template.app');

  if (!fs.existsSync(templateApp)) {
    log.err(`Mac app template missing: ${templateApp}`);
    log.err(`Build it once (maintainer step) with:`);
    log.err(`  bash protovibe-project-manager/scripts/build-mac-template.sh`);
    log.err(`That script needs Platypus: brew install --cask platypus`);
    process.exit(1);
  }

  fs.mkdirSync(installerDir, { recursive: true });
  if (fs.existsSync(appPath)) {
    fs.rmSync(appPath, { recursive: true, force: true });
  }

  // ~/.protovibe/launch.sh is what the bundle's shim execs — regenerated on
  // every install so project-path moves and updater changes propagate.
  writeMacLauncherScript();

  // Duplicate the prebuilt Platypus template (Cocoa app w/ Text Window UI,
  // icon, AppSettings.plist, MainMenu.nib all baked in). ditto preserves the
  // executable bit on Contents/MacOS/Protovibe and Resources/script — cp -R
  // sometimes loses it on cross-fs copies.
  log.info('  → Copying Protovibe.app from template …');
  try {
    execSync(`ditto ${JSON.stringify(templateApp)} ${JSON.stringify(appPath)}`, { stdio: 'pipe' });
  } catch (e) {
    log.err(`ditto failed: ${e.message}`);
    process.exit(1);
  }

  // Strip quarantine before signing in case the repo was downloaded as a zip
  // (git clone doesn't quarantine, browser downloads do — and the xattr
  // propagates into ditto's copy).
  try {
    execSync(`xattr -dr com.apple.quarantine ${JSON.stringify(appPath)}`, { stdio: 'pipe' });
  } catch {}

  // Ad-hoc codesign so Gatekeeper is happier on first launch. The bundle is
  // locally-assembled and unquarantined, so this combination is enough for
  // double-click to work without a right-click → Open dance.
  log.info('  → Signing …');
  try {
    execSync(`codesign --force --deep --sign - ${JSON.stringify(appPath)}`, { stdio: 'pipe' });
  } catch (e) {
    log.warn(`Ad-hoc codesign failed: ${e.message.split('\n')[0]}`);
  }
  try { execSync(`touch ${JSON.stringify(appPath)}`); } catch {} // refresh icon cache

  // Symlink to /Applications (idempotent)
  try {
    if (fs.lstatSync(symlinkPath, { throwIfNoEntry: false })) {
      fs.rmSync(symlinkPath, { force: true });
    }
  } catch {}
  try {
    fs.symlinkSync('/Applications', symlinkPath, 'dir');
  } catch (e) {
    log.warn(`Failed to create /Applications symlink: ${e.message}`);
  }

  // Drop in the pre-baked .DS_Store so the DMG volume opens with the
  // classic drag-to-Applications layout. Mounted DMGs are read-only, so
  // Finder respects this layout reliably (unlike a plain folder, where it
  // gets clobbered by Finder's in-memory state).
  const dsStoreTemplate = path.join(ASSETS_DIR, 'installer.DS_Store');
  if (fs.existsSync(dsStoreTemplate)) {
    try {
      fs.copyFileSync(dsStoreTemplate, path.join(installerDir, '.DS_Store'));
    } catch (e) {
      log.warn(`Failed to apply installer .DS_Store: ${e.message}`);
    }
  }

  // Build a real DMG from the staged folder and mount it. This gives the
  // user the canonical drag-to-install window — and bakes in the layout.
  const dmgVolName = `${APP_NAME} Installer`;
  const dmgPath = path.join(installerDir, `${dmgVolName}.dmg`);
  const volPath = `/Volumes/${dmgVolName}`;

  // Unmount any prior mount of the same volume name so hdiutil doesn't
  // append " 1" to the new mount.
  try { execSync(`hdiutil detach ${JSON.stringify(volPath)} -force`, { stdio: 'pipe' }); } catch {}
  try { fs.rmSync(dmgPath, { force: true }); } catch {}

  // Build the DMG into the OS tmpdir first so hdiutil's snapshot of
  // installerDir doesn't accidentally include the .dmg in its own payload.
  const dmgTmp = path.join(os.tmpdir(), `protovibe-${process.pid}-${Date.now()}.dmg`);
  log.info('  → Building installer disk image (this takes ~10s) …');
  try {
    execSync(
      `hdiutil create -volname ${JSON.stringify(dmgVolName)} -srcfolder ${JSON.stringify(installerDir)} -ov -format UDZO ${JSON.stringify(dmgTmp)}`,
      { stdio: 'pipe' }
    );
    fs.renameSync(dmgTmp, dmgPath);
  } catch (e) {
    log.err(`hdiutil create failed: ${e.message}`);
    try { fs.rmSync(dmgTmp, { force: true }); } catch {}
    process.exit(1);
  }

  // Mount the DMG. -noautoopen suppresses Finder's auto window so we can
  // open it ourselves once it's ready (Finder otherwise sometimes opens
  // before the volume is fully attached).
  log.info('  → Mounting installer …');
  try {
    execSync(`hdiutil attach ${JSON.stringify(dmgPath)} -noverify -noautoopen`, { stdio: 'pipe' });
  } catch (e) {
    log.warn(`Could not mount DMG automatically: ${e.message.split('\n')[0]}`);
    log.info(`Open it manually: ${dmgPath}`);
  }

  log.info('  → Opening Finder window …');
  spawnSync('open', [volPath], { stdio: 'ignore' });

  log.ok(`Created ${APP_NAME}.app at ${appPath}`);
  log.info(`Drag ${APP_NAME}.app onto Applications in the window that just opened.`);
}

// ───────────────────────────── Windows ───────────────────────────
function findCsc() {
  // csc.exe ships with .NET Framework 4 — present on every Windows since 2010.
  // Prefer 64-bit; fall back to 32-bit. Returns null if neither exists.
  const candidates = [
    'C:\\Windows\\Microsoft.NET\\Framework64\\v4.0.30319\\csc.exe',
    'C:\\Windows\\Microsoft.NET\\Framework\\v4.0.30319\\csc.exe',
  ];
  for (const p of candidates) if (fs.existsSync(p)) return p;
  return null;
}

function buildWindowsExe(launcherDir, iconPath) {
  const csc = findCsc();
  if (!csc) return null;
  const cs = path.join(__dirname, 'launcher', 'Protovibe.cs');
  if (!fs.existsSync(cs)) return null;
  const exe = path.join(launcherDir, 'Protovibe.exe');
  const args = [
    '/nologo', '/target:exe', '/optimize+', '/codepage:65001',
    `/out:${exe}`,
  ];
  if (iconPath) args.push(`/win32icon:${iconPath}`);
  args.push(cs);
  try {
    // Use spawnSync so paths with spaces don't need shell quoting.
    const r = spawnSync(csc, args, { stdio: 'pipe', encoding: 'utf8' });
    if (r.status !== 0) {
      log.warn(`csc.exe failed (exit ${r.status}): ${(r.stderr || r.stdout || '').trim().split('\n')[0]}`);
      return null;
    }
    return exe;
  } catch (e) {
    log.warn(`csc.exe invocation failed: ${e.message}`);
    return null;
  }
}

function createWindowsShortcut() {
  const desktop = resolveDesktopDir();
  const launcherDir = PROTOVIBE_CONFIG_DIR;
  const launcherBat = path.join(launcherDir, 'Protovibe.bat');
  const lnkPath = path.join(desktop, `${APP_NAME}.lnk`);
  const iconPath = checkIcon('icon.ico');

  fs.mkdirSync(launcherDir, { recursive: true });

  // Preferred path: compile a tiny C# launcher exe with the icon embedded as a
  // Win32 resource. The shortcut + taskbar + Alt-Tab all pick up that icon
  // instead of the generic cmd.exe one. Falls back to the .bat launcher if
  // csc.exe is unavailable (vanishingly rare, but handled).
  const exePath = buildWindowsExe(launcherDir, iconPath);
  if (exePath) {
    // Launch via conhost.exe to bypass Windows Terminal — when a .lnk targets a
    // console exe directly on Win11, Windows hosts it in the user's default
    // terminal (usually Windows Terminal), which uses its own icon and opens
    // tabs. Routing through conhost forces the classic single-window console
    // and lets the hosted exe's embedded icon appear in the taskbar.
    // We can't override the user's GLOBAL default terminal (that lives under
    // HKCU\Console\%%Startup) without affecting other apps, and per-app
    // delegation isn't supported. The shortcut indirection is the cleanest
    // user-scoped fix.
    const conhost = path.join(process.env.SystemRoot || 'C:\\Windows', 'System32', 'conhost.exe');
    createWindowsShortcutLnk({
      target: conhost,
      args: `"${exePath}"`,
      workingDir: launcherDir,
      lnkPath,
      // IconLocation is needed because the .lnk now targets conhost, not our
      // exe. Point at our exe so the desktop shortcut shows the Protovibe
      // icon. The running window's taskbar icon comes from the hosted exe
      // (Protovibe.exe), which has its own icon resources embedded.
      iconPath: exePath,
    });
    // Drop the legacy bat so re-installs don't leave a stale launcher behind.
    for (const f of [launcherBat, path.join(launcherDir, 'banner.ps1')]) {
      try { fs.rmSync(f, { force: true }); } catch {}
    }
    log.ok(`Launcher built: ${exePath}`);
    log.ok(`Shortcut created: ${lnkPath}`);
    return;
  }
  log.warn('csc.exe not available — falling back to .bat launcher (taskbar will show generic terminal icon).');

  const PROTOVIBE_URL = 'http://127.0.0.1:5173';

  // Banner written as a separate .ps1 so Unicode block chars survive with correct
  // encoding (UTF-8 BOM). Embedding them in a -Command arg fails because cmd.exe
  // reads .bat files using the system ANSI code page, corrupting the multi-byte
  // UTF-8 sequences before PowerShell ever sees them.
  const bannerPs1Path = path.join(launcherDir, 'banner.ps1');
  const bannerPs1 = [
    `$OutputEncoding = [Console]::OutputEncoding = [System.Text.Encoding]::UTF8`,
    `Write-Host ''`,
    `Write-Host '   ▄▄▄▄▄▄▄▄                                        ▄▄' -ForegroundColor Cyan`,
    `Write-Host '   ███▀▀▀███                ██                 ▀▀  ██' -ForegroundColor Cyan`,
    `Write-Host '   ███   ███ ████▄ ▄█████▄ ▀██▀▀ ▄█████▄ ██ ██ ██  ████▄ ▄█▀█▄' -ForegroundColor Cyan`,
    `Write-Host '   ███  ▀▀▀  ██ ▀▀ ██   ██  ██   ██   ██ ██▄██ ██  ██ ██ ██▄█▀' -ForegroundColor Cyan`,
    `Write-Host '   ███       ██    ▀█████▀  ██   ▀█████▀  ▀█▀  ██▄ ████▀ ▀█▄▄▄' -ForegroundColor Cyan`,
    `Write-Host ''`,
    `Write-Host '   Keep this window open while using Protovibe in your browser.' -ForegroundColor Yellow`,
    `Write-Host ''`,
  ].join('\r\n');
  // UTF-8 BOM prefix so PowerShell reads this file as UTF-8 on all Windows locales
  fs.writeFileSync(bannerPs1Path, '﻿' + bannerPs1, 'utf8');

  // Launcher reads project path from config file at runtime (path-independent).
  // Mirrors the Mac launch.sh UX: banner, double-server guard, status messages.
  const batLines = [
    '@echo off',
    'setlocal EnableDelayedExpansion',
    'title Protovibe',
    '',
    'set "CFG=%USERPROFILE%\\.protovibe\\project-path"',
    'if not exist "%CFG%" (',
    '  echo Protovibe is not configured.',
    '  echo Run install.bat from the Protovibe project directory first.',
    '  pause',
    '  exit /b 1',
    ')',
    'set /p PROJECT_ROOT=<"%CFG%"',
    'if not exist "%PROJECT_ROOT%" (',
    '  echo Protovibe folder not found at: %PROJECT_ROOT%',
    '  echo If you moved it, re-run install.bat from the new location.',
    '  pause',
    '  exit /b 1',
    ')',
    '',
    'cls',
    // Call the pre-written banner.ps1 (UTF-8 BOM) — avoids ANSI code page corruption
    `powershell -NoProfile -ExecutionPolicy Bypass -File "%USERPROFILE%\\.protovibe\\banner.ps1"`,
    '',
    // Already-running guard: hit the project-manager API; if it responds, just open browser
    `powershell -NoProfile -ExecutionPolicy Bypass -Command "try { Invoke-WebRequest -UseBasicParsing -TimeoutSec 2 -Uri '${PROTOVIBE_URL}/api/projects' | Out-Null; exit 0 } catch { exit 1 }" >nul 2>&1`,
    'if not errorlevel 1 (',
    `  powershell -NoProfile -ExecutionPolicy Bypass -Command "Write-Host '   Already running -- opening browser...' -ForegroundColor Yellow; Write-Host ''"`,
    `  start "" "${PROTOVIBE_URL}"`,
    '  exit /b 0',
    ')',
    '',
    'set "NODE_NO_WARNINGS=1"',
    'cd /d "%PROJECT_ROOT%"',
    '',
    'rem Apply a staged project-manager update if one is pending. The updater',
    'rem writes to protovibe-project-manager.pending\\ so the running vite never',
    'rem sees its own files change. Swap dirs here and reconcile deps.',
    'if exist "protovibe-project-manager.pending" (',
    `  powershell -NoProfile -ExecutionPolicy Bypass -Command "Write-Host '   Applying staged project-manager update...' -ForegroundColor Cyan"`,
    '  if exist "protovibe-project-manager\\node_modules" if not exist "protovibe-project-manager.pending\\node_modules" (',
    '    move "protovibe-project-manager\\node_modules" "protovibe-project-manager.pending\\node_modules" >nul',
    '  )',
    '  if exist "protovibe-project-manager.old" rmdir /s /q "protovibe-project-manager.old"',
    '  move "protovibe-project-manager" "protovibe-project-manager.old" >nul',
    '  move "protovibe-project-manager.pending" "protovibe-project-manager" >nul',
    '  if exist "protovibe-project-manager.old" rmdir /s /q "protovibe-project-manager.old"',
    `  powershell -NoProfile -ExecutionPolicy Bypass -Command "Write-Host '   Installing dependencies...' -ForegroundColor Cyan"`,
    '  call pnpm --dir protovibe-project-manager install --prefer-offline',
    ')',
    '',
    `powershell -NoProfile -ExecutionPolicy Bypass -Command "Write-Host '   Open your browser and go to URL: ${PROTOVIBE_URL}' -ForegroundColor Cyan; Write-Host ''"`,
    'call pnpm --dir protovibe-project-manager dev',
    'if errorlevel 1 (',
    '  echo.',
    '  echo Dev server exited with an error.',
    '  pause',
    ')',
  ];
  fs.writeFileSync(launcherBat, batLines.join('\r\n') + '\r\n', 'utf8');

  createWindowsShortcutLnk({ target: launcherBat, workingDir: launcherDir, lnkPath, iconPath });

  // Clean up legacy in-repo bat from older installs
  const legacyBat = path.join(PROJECT_ROOT, 'start-protovibe.bat');
  if (fs.existsSync(legacyBat)) { try { fs.rmSync(legacyBat, { force: true }); } catch {} }

  log.ok(`Shortcut created: ${lnkPath}`);
  log.info(`Launcher: ${launcherBat}`);
}

function createWindowsShortcutLnk({ target, args, workingDir, lnkPath, iconPath }) {
  if (fs.existsSync(lnkPath)) fs.rmSync(lnkPath, { force: true });

  const psEscape = (s) => s.replace(/`/g, '``').replace(/"/g, '`"');
  const psLines = [
    `$WshShell = New-Object -ComObject WScript.Shell`,
    `$Shortcut = $WshShell.CreateShortcut("${psEscape(lnkPath)}")`,
    `$Shortcut.TargetPath = "${psEscape(target)}"`,
    `$Shortcut.WorkingDirectory = "${psEscape(workingDir)}"`,
    `$Shortcut.WindowStyle = 1`,
    `$Shortcut.Description = "Run Protovibe Project Manager"`,
  ];
  if (args) psLines.push(`$Shortcut.Arguments = "${psEscape(args)}"`);
  // For the EXE path the icon is already embedded as a Win32 resource, so we
  // skip IconLocation and let Windows pull it from the binary.
  if (iconPath) psLines.push(`$Shortcut.IconLocation = "${psEscape(iconPath)}"`);
  psLines.push(`$Shortcut.Save()`);

  const psFile = path.join(workingDir, '_create-shortcut.ps1');
  fs.writeFileSync(psFile, psLines.join('\r\n'), 'utf8');

  try {
    execSync(`powershell.exe -ExecutionPolicy Bypass -NoProfile -File "${psFile}"`, { stdio: 'pipe' });
  } catch (e) {
    log.err(`PowerShell shortcut creation failed: ${e.message}`);
    log.err(`Hint: Windows Script Host may be disabled by group policy.`);
    fs.rmSync(psFile, { force: true });
    process.exit(1);
  }
  fs.rmSync(psFile, { force: true });
}

// ───────────────────────────── Linux ─────────────────────────────
function createLinuxShortcut() {
  const desktop = resolveDesktopDir();
  const desktopFile = path.join(desktop, `${APP_NAME}.desktop`);
  const iconPath = checkIcon('icon.png');

  // Path-independent: read config at runtime.
  const execLine = `bash -c 'CFG="$HOME/.protovibe/project-path"; if [ ! -f "$CFG" ]; then echo "Protovibe not configured. Run ./install.sh first."; exec bash; fi; ROOT=$(cat "$CFG"); if [ ! -d "$ROOT" ]; then echo "Protovibe folder not found at $ROOT. Re-run ./install.sh."; exec bash; fi; case ":$PATH:" in *":$HOME/.local/bin:"*) ;; *) export PATH="$HOME/.local/bin:$PATH" ;; esac; cd "$ROOT" && pnpm --dir protovibe-project-manager dev 2>&1 | tee "$ROOT/dev.log"; exec bash'`;

  const lines = [
    `[Desktop Entry]`,
    `Version=1.0`,
    `Type=Application`,
    `Name=${APP_NAME}`,
    `Comment=Run Protovibe Project Manager`,
    `Exec=${execLine}`,
    `Terminal=true`,
    `Categories=Development;`,
  ];
  if (iconPath) lines.push(`Icon=${iconPath}`);

  fs.writeFileSync(desktopFile, lines.join('\n') + '\n', 'utf8');
  try { fs.chmodSync(desktopFile, 0o755); } catch (e) { log.warn(`chmod failed: ${e.message}`); }

  // Mark as trusted on GNOME — best-effort, ignore if gio missing.
  try {
    execSync(`gio set "${desktopFile}" metadata::trusted true`, { stdio: 'pipe' });
  } catch {}

  log.ok(`Desktop entry created: ${desktopFile}`);
}

// ─────────────────────────────  main  ────────────────────────────
function main() {
  const platform = os.platform();
  log.info(`Detected platform: ${platform}`);
  log.info(`Project root: ${PROJECT_ROOT}`);

  writeProjectPathConfig();

  switch (platform) {
    case 'darwin': createMacShortcut(); break;
    case 'win32':  createWindowsShortcut(); break;
    case 'linux':  createLinuxShortcut(); break;
    default:
      log.err(`Unsupported platform: ${platform}`);
      process.exit(1);
  }
}

main();
